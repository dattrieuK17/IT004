SELECT A.MAHV, HOTEN FROM
(
	SELECT HV.MAHV, HO + TEN AS HOTEN, LANTHI
	FROM HOCVIEN HV INNER JOIN KETQUATHI KQT 
	ON HV.MAHV = KQT.MAHV
	WHERE LEFT(MALOP, 1) = 'K' AND KQUA = 'Khong Dat' AND MAMH = 'CTRR'
) A
INNER JOIN
(
	SELECT MAHV, MAX(LANTHI) LANTHIMAX  
	FROM KETQUATHI
	WHERE MAMH = 'CTRR' 
	GROUP BY MAHV
) B
ON A.MAHV = B.MAHV
WHERE LANTHI = LANTHIMAX

